public class Cashier
{
    private String name;
    // Constructor to initialize the Cashier with a name
    public Cashier(String name) {
        this.name = name;
    }

    // Getter method to retrieve the name of the Cashier
    public String getName() {
        return name;
    }

    // Setter method to set the name of the Cashier
    public void setName(String name) {
        this.name = name;
    }
}
